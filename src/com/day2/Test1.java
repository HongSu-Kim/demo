package com.day2;

//public : 접근지정자(public,private 등)
//클래스 이름 : 첫글자는 대문자(예 : CustomInfo)
//클래스의 이름과 파일의 이름은 일치
public class Test1 {

	// method : 명령어
	// 메인메소드
	// void : return 값 없음
	// 메소드 이름 : 첫글자 소문자(예 : customInfo)
	// static 자동으로 메모리에 올라감
	public static void main(String[] args) {

		// ()안에는 인수(매개변수)가 들어간다.
		// print는 매개변수를 입력해주어야
		// println은 매개변수를 생략가능
		// \n은 줄바꿈
		System.out.print("오늘은 금요일~~~\n\n"); // string은 "", char는 ''
		System.out.println();
		System.out.println("불금입니다...");

		System.out.print("a");
		System.out.println();

	}

}
